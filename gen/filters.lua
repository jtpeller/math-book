-- Convert .md links to .html
function Link(el)
    -- Check if the link target ends with .md
    if el.target:match("%.md$") then
        -- Replace .md with .html
        el.target = el.target:gsub("%.md$", ".html")
    end
    return el
end

-- Force Images to Center
function Image(el)
    -- Ensure attributes table exists
    el.attr = el.attr or pandoc.Attr()

    -- 1. Remove old align, if it exists
    if el.attr.attributes['align'] then
        el.attr.attributes['align'] = nil
    end

    -- 2. Force CSS Centering
    -- display:block and margin:auto is the most reliable way to center images in HTML
    local base_style = "display: block; margin-left: auto; margin-right: auto; text-align: center;"
    el.attr.attributes['style'] = base_style .. (el.attr.attributes['style'] or "")

    return el
end

-- Remove the 'align' attribute.
function Block(el)
    if el.attr and el.attr.attributes['align'] then
        local alignment = el.attr.attributes['align']
        el.attr.attributes['align'] = nil
        -- Apply the alignment via inline CSS instead
        el.attr.attributes['style'] = (el.attr.attributes['style'] or "") .. "text-align: " .. alignment .. ";"
    end
    return el
end

-- Force Tables to Center and add Summary
function Table(el)
    -- Ensure attributes table exists
    el.attr = el.attr or pandoc.Attr()

    -- 1. Add the Summary attribute (useful for screen readers/accessibility)
    -- You can make this dynamic based on the caption if needed
    el.attr.attributes['aria-label'] = "Data table generated by Pandoc"
    el.attr.attributes['summary'] = "Data table generated by Pandoc"

    -- 2. Force CSS Centering
    -- For tables, margin:auto on the element itself usually handles centering
    local table_style = "margin-left: auto; margin-right: auto; border-collapse: collapse;"
    el.attr.attributes['style'] = table_style .. (el.attr.attributes['style'] or "")

    return el
end

-- LaTeX Color & Style Fix
function Math(el)
    -- If it doesn't include \textcolor, allow mathtex to render it.
    if not el.text:find("\\textcolor") then
        return el
    end

    -- Try to match the \textit version
    -- Format: \textcolor{color}{\textit{word}}
    local color, text_it = el.text:match("\\textcolor%{(.-)%}%{\\textit%{(.-)%}%}")
    if color and text_it then
        return pandoc.Span(text_it, {
            style = "color: var(--color-definition); font-style: italic;"
        })
    end

    -- Try to match the \textbf version
    -- Format: \textcolor{color}{\textbf{word}}
    local color_b, text_bf = el.text:match("\\textcolor%{(.-)%}%{\\textbf%{(.-)%}%}")
    if color_b and text_bf then
        return pandoc.Span(text_bf, {
            style = "color: var(--color-definition); font-weight: bold;"
        })
    end

    -- Fallback for plain \textcolor{color}{word}
    local color_p, text_p = el.text:match("\\textcolor%{(.-)%}%{(.-)%}")
    if color_p and text_p then
        return pandoc.Span(text_p, {
            style = "color: var(--color-definition);"
        })
    end

    -- If no match at all, return the original element
    return el
end

function BlockQuote(el)
    -- Get the first inline element of the first block of the blockquote
    local first_block = el.content[1]
    if first_block and first_block.t == "Para" then
        local first_inline = first_block.content[1]

        -- Check if it matches the [!TYPE] pattern
        if first_inline and first_inline.t == "Str" then
            local alert_type = first_inline.text:match("^%[!([%a%d]+)%]$")

            if alert_type then
                -- Lowercase the type for CSS consistency (note, warning, etc.)
                local type_class = alert_type:lower()

                -- Remove the [!TYPE] marker from the content
                table.remove(first_block.content, 1)
                -- Remove the space following the marker if it exists
                if first_block.content[1] and first_block.content[1].t == "Space" then
                    table.remove(first_block.content, 1)
                end

                -- Return a Div with the content and a specific class
                return pandoc.Div(el.content, {
                    class = "markdown-alert alert-" .. type_class
                })
            end
        end
    end
    return el
end
